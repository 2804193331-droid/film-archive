function splitCamera(name: string, type: CameraRecord["type"]): CameraRecord {
  const [brand, ...rest] = name.split(" ");
  return {
    brand,
    model: rest.join(" "),
    type
  };
}

function lens(brand: string, mount: string, model: string, focalLength: string): LensRecord {
  return { brand, mount, model, focalLength };
}
